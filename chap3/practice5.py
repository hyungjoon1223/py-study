#탈출문자   
# \n : 줄바꿈
print("백문이 불여일견 \n백견이 불여일타")  

# 저는 "황형준" 입니다.
# \", \' : 문장 내에서 따옴표 사용이 가능하게 만들어줌
print("저는 '황형준'입니다.")
print('저는 "황형준"입니다.')
print("저는 \"황형준\" 입니다")
print("저는 \'황형준\' 입니다")

# \\ : 문장 내에서 하나의 \만 나오게 됨.
#print("C:\Users\형듀닝\Desktop\my python> ")
print("C:\\Users\\형듀닝\\Desktop\\my python> ")

# \r : 커서를 맨 앞으로 이동
print("Red Apple\rPine")  #PineApple 출력

# \b : 백스페이스 (한 글자 삭제)
print("Redd\bApple")

# \t : 탭
print("Red\tApple")  #Red   Apple